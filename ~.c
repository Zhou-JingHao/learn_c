#include <stdio.h>

int main()
{
    int a=0;
    int b=~a;
    
    printf("%d\n",a);
    printf("%d\n",b); //按位取反 （二进制）
    // 00000000
    // 变成
    // 11111111
    //负数在存储的时候是二进制的补码
    //原码，反码，补码

    //11111111(补码)
    //11111110(反码)减1
    //10000001(源码)符号位不变，按位取反
    //打印用的是源码
    system("pause");
}